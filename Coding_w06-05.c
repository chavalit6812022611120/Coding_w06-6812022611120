#include <stdio.h> // สำหรับฟังก์ชัน printf

int main() {
    printf("หัวข้อ: ลำดับการประเมินผลของนิพจน์ (Order of Evaluation in C Expressions)\n\n");

    // นิพจน์ A: A = -2 + 5 * 2;
    // ลำดับการคำนวณ:
    // 1. 5 * 2 = 10 (คูณมีลำดับความสำคัญสูงกว่าบวก/ลบ)
    // 2. -2 + 10 = 8
    int A = -2 + 5 * 2;
    printf("A = -2 + 5 * 2;\n");
    printf("   วิธีคิด: -2 + (5 * 2) = -2 + 10 = %d\n", A);
    printf("   ผลลัพธ์: A = %d\n\n", A);

    // นิพจน์ B: B = 10 / 2 * 3;
    // ลำดับการคำนวณ:
    // 1. 10 / 2 = 5 (หารและคูณมีลำดับความสำคัญเท่ากัน ประเมินจากซ้ายไปขวา)
    // 2. 5 * 3 = 15
    int B = 10 / 2 * 3;
    printf("B = 10 / 2 * 3;\n");
    printf("   วิธีคิด: (10 / 2) * 3 = 5 * 3 = %d\n", B);
    printf("   ผลลัพธ์: B = %d\n\n", B);

    // นิพจน์ C: C = 6 / 2 + 3 * (4 % 2);
    // ลำดับการคำนวณ:
    // 1. (4 % 2) = 0 (วงเล็บก่อน)
    // 2. 3 * 0 = 0 (คูณ)
    // 3. 6 / 2 = 3 (หาร)
    // 4. 3 + 0 = 3 (บวก)
    int C = 6 / 2 + 3 * (4 % 2);
    printf("C = 6 / 2 + 3 * (4 %% 2);\n"); // ใช้ %% เพื่อพิมพ์ %
    printf("   วิธีคิด: 6 / 2 + 3 * (0) = 3 + 0 = %d\n", C);
    printf("   ผลลัพธ์: C = %d\n\n", C);

    // นิพจน์ D: D = (5 + 2) * 15 % 4;
    // ลำดับการคำนวณ:
    // 1. (5 + 2) = 7 (วงเล็บก่อน)
    // 2. 7 * 15 = 105 (คูณและ Modulo มีลำดับความสำคัญเท่ากัน ประเมินจากซ้ายไปขวา)
    // 3. 105 % 4 = 1 (105 หาร 4 ได้ 26 เหลือเศษ 1)
    int D = (5 + 2) * 15 % 4;
    printf("D = (5 + 2) * 15 %% 4;\n"); // ใช้ %% เพื่อพิมพ์ %
    printf("   วิธีคิด: (7) * 15 %% 4 = 105 %% 4 = %d\n", D);
    printf("   ผลลัพธ์: D = %d\n\n", D);

    // นิพจน์ E: E = 6 + 2 * 2 - 6 / 2;
    // ลำดับการคำนวณ:
    // 1. 2 * 2 = 4 (คูณ)
    // 2. 6 / 2 = 3 (หาร)
    // 3. 6 + 4 = 10 (บวก/ลบ มีลำดับความสำคัญเท่ากัน ประเมินจากซ้ายไปขวา)
    // 4. 10 - 3 = 7
    int E = 6 + 2 * 2 - 6 / 2;
    printf("E = 6 + 2 * 2 - 6 / 2;\n");
    printf("   วิธีคิด: 6 + (2 * 2) - (6 / 2) = 6 + 4 - 3 = 10 - 3 = %d\n", E);
    printf("   ผลลัพธ์: E = %d\n\n", E);

    // นิพจน์ F: F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // ลำดับการคำนวณ:
    // 1. (6 % 5) = 1 (วงเล็บ)
    // 2. 3 * 2 = 6 (คูณ)
    // 3. 8 / 4 = 2 (หาร)
    // 4. 5 + 6 = 11 (บวก/ลบ ซ้ายไปขวา)
    // 5. 11 - 2 = 9
    // 6. 9 + 1 = 10
    int F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = 5 + 3 * 2 - 8 / 4 + (6 %% 5);\n"); // ใช้ %% เพื่อพิมพ์ %
    printf("   วิธีคิด: 5 + (3 * 2) - (8 / 4) + (6 %% 5) = 5 + 6 - 2 + 1 = 11 - 2 + 1 = 9 + 1 = %d\n", F);
    printf("   ผลลัพธ์: F = %d\n\n", F);

    // นิพจน์ G: G = (4 + 3) * 2 - 10 / (2 + 3);
    // ลำดับการคำนวณ:
    // 1. (4 + 3) = 7 (วงเล็บแรก)
    // 2. (2 + 3) = 5 (วงเล็บสอง)
    // 3. 7 * 2 = 14 (คูณ)
    // 4. 10 / 5 = 2 (หาร)
    // 5. 14 - 2 = 12 (ลบ)
    int G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = (4 + 3) * 2 - 10 / (2 + 3);\n");
    printf("   วิธีคิด: (7) * 2 - 10 / (5) = 14 - 2 = %d\n", G);
    printf("   ผลลัพธ์: G = %d\n\n", G);

    return 0; // บอกว่าโปรแกรมทำงานเสร็จสมบูรณ์
}