#include <stdio.h> // เรียกใช้ไลบรารีสำหรับฟังก์ชัน printf (แสดงผล)

int main() { // จุดเริ่มต้นของโปรแกรม
    // --- ส่วนที่ 1: การคำนวณตัวเลขจำนวนเต็ม ---
    int i = 1, j = 2, k; // กำหนดตัวแปรจำนวนเต็ม i=1, j=2, k ยังไม่กำหนดค่า
    printf(" 1: i = %d, j = %d, k = k(ยังไม่ได้กำหนดค่า)\n", i, j); // แสดงค่าเริ่มต้น i=1, j=2, k (ไม่ได้กำหนด)

    k = i + j; // k = 1 + 2 = 3
    printf(" 2: i = %d, j = %d, k = %d\n", i, j, k); // แสดงค่า i=1, j=2, k=3

    i = i + (k * j); // i = 1 + (3 * 2) = 1 + 6 = 7
    printf(" 3: i = %d, j = %d, k = %d\n", i, j, k); // แสดงค่า i=7, j=2, k=3

    j = i / 2; // j = 7 / 2 = 3 (หารจำนวนเต็ม เศษทิ้ง)
    printf(" 4: i = %d, j = %d, k = %d\n", i, j, k); // แสดงค่า i=7, j=3, k=3

    k = i % 2; // k = 7 หารเอาเศษ 2 = 1
    printf(" 5: i = %d, j = %d, k = %d\n", i, j, k); // แสดงค่า i=7, j=3, k=1

    i = (j + k) * 3; // i = (3 + 1) * 3 = 4 * 3 = 12
    printf(" 6: i = %d, j = %d, k = %d\n", i, j, k); // แสดงค่า i=12, j=3, k=1

    printf("ตาราง2 %d\n"); // แสดงข้อความ "ตาราง2" (มี %d แต่ไม่ได้ส่งค่าอะไรไป)

    // --- ส่วนที่ 2: การคำนวณตัวเลขทศนิยม ---
    double x = 1.0, y = 2.0, z; // กำหนดตัวแปรทศนิยม x=1.0, y=2.0, z ยังไม่กำหนดค่า
    printf(" 1: x = %.1f, y = %.1f, z = z(ยังไม่ได้กำหนดค่า)\n", x, y); // แสดงค่าเริ่มต้น x=1.0, y=2.0, z (ไม่ได้กำหนด)

    x = y + 5.0; // x = 2.0 + 5.0 = 7.0
    printf(" 2: x = %.1f, y = %.1f, z = z(ยังไม่ได้กำหนดค่า)\n", x, y); // แสดงค่า x=7.0, y=2.0, z (ไม่ได้กำหนด)

    y = x / 2.0; // y = 7.0 / 2.0 = 3.5
    printf(" 3: x = %.1f, y = %.1f, z = z(ยังไม่ได้กำหนดค่า)\n", x, y); // แสดงค่า x=7.0, y=3.5, z (ไม่ได้กำหนด)

    y = (x * 3.0) + 4.0; // y = (7.0 * 3.0) + 4.0 = 21.0 + 4.0 = 25.0
    printf(" 4: x = %.1f, y = %.1f, z = z(ยังไม่ได้กำหนดค่า)\n", x, y); // แสดงค่า x=7.0, y=25.0, z (ไม่ได้กำหนด)

    x = -0.5 - y; // x = -0.5 - 25.0 = -25.5
    printf(" 5: x = %.1f, y = %.1f, z = z(ยังไม่ได้กำหนดค่า)\n", x, y); // แสดงค่า x=-25.5, y=25.0, z (ไม่ได้กำหนด)

    z = x + y; // z = -25.5 + 25.0 = -0.5
    printf(" 6: x = %.1f, y = %.1f, z = %.1f\n", x, y, z); // แสดงค่า x=-25.5, y=25.0, z=-0.5

    return 0; // จบโปรแกรม
}