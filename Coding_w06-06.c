#include <stdio.h> // ใช้สำหรับฟังก์ชันมาตรฐานอย่าง printf() เพื่อแสดงผลข้อมูล

int main() { // นี่คือจุดเริ่มต้นของโปรแกรม
    // ประกาศและกำหนดค่าเริ่มต้นให้กับตัวแปรจำนวนเต็ม (integers)
    int a = 5, b = 2;
    // ประกาศและกำหนดค่าเริ่มต้นให้กับตัวแปรทศนิยม (floating-point numbers)
    float x = 3.0, y = 4.5;

    // คำนวณและแสดงผล r1: การใช้ตัวดำเนินการหลังเพิ่มค่า (a++), การคูณ, การแปลงชนิดข้อมูล, และหารเอาเศษ
    // a (5) ถูกใช้ก่อน แล้วค่อยเพิ่มเป็น 6 -> (5 * 2) + (int)(4.5) % 3 -> 10 + 4 % 3 -> 10 + 1 = 11
    int r1 = a++ * b + (int)y % 3;
    printf("r1 %d\n" , r1 ); // ผลลัพธ์: r1 11

    // คำนวณและแสดงผล r2: การใช้ตัวดำเนินการเปรียบเทียบและการดำเนินการตรรกะแบบ AND (&&)
    // (a (6) > b (2)) เป็นจริง และ ((int)x (3) / b (2) < 2) ซึ่งคือ (1 < 2) เป็นจริง
    // จริง && จริง ได้ผลลัพธ์เป็น 1 (True)
    int r2 = (a > b) && ((int)x / b < 2);
    printf("r2 = %d\n" , r2); // ผลลัพธ์: r2 = 1

    // คำนวณและแสดงผล r3: การใช้ตัวดำเนินการก่อนเพิ่มค่า (++x), การคูณ, และการลบ (หารจำนวนเต็ม)
    // x (3.0) ถูกเพิ่มเป็น 4.0 ก่อนนำไปใช้ -> (4.0 * 4.5) - (a (6) / 2) -> 18.0 - 3 = 15.0
    float r3 = (++x * y - a / 2);
    printf("r3 = %.2f\n" ,r3); // ผลลัพธ์: r3 = 15.00

    // คำนวณและแสดงผล r4: การใช้ตัวดำเนินการเพิ่มค่าแบบย่อ (+=), การเปรียบเทียบ, และการดำเนินการตรรกะแบบ OR (||)
    // x (4.0) เพิ่ม 1.5 เป็น 5.5 -> (5.5 > y (4.5)) เป็นจริง
    // เนื่องจากเงื่อนไขแรกของ || เป็นจริง เงื่อนไขที่สอง (b-- > 0) จะไม่ถูกประเมิน
    // ผลลัพธ์เป็น 1.0 (True)
    float r4 = ((x += 1.5) > y) || (b-- > 0);
    printf("r4 = %.2f\n" ,r4); // ผลลัพธ์: r4 = 1.00

    return 0; // โปรแกรมจบการทำงานอย่างสมบูรณ์
}
